import 'package:flutter/material.dart';

/// 支持的语言
enum AppLanguage {
  zh, // 中文
  ko, // 한국어
}

/// 语言切换控制器
class LanguageProvider extends ChangeNotifier {
  AppLanguage _lang = AppLanguage.zh;

  AppLanguage get current => _lang;

  String get currentLabel {
    switch (_lang) {
      case AppLanguage.zh:
        return '中';
      case AppLanguage.ko:
        return '한';
    }
  }

  void setLanguage(AppLanguage lang) {
    if (_lang == lang) return;
    _lang = lang;
    notifyListeners();
  }

  /// 通过 key 获取当前语言下的翻译文本
  String t(String key) => _translations[_lang]?[key] ?? key;

  /// 翻译数据内容（中文原文 → 当前语言）
  String ct(String zhText) {
    if (_lang == AppLanguage.zh) return zhText;
    return _koContent[zhText] ?? zhText;
  }

  static LanguageProvider of(BuildContext context) {
    return context
        .dependOnInheritedWidgetOfExactType<_LanguageScope>()!
        .provider;
  }
}

/// InheritedWidget 提供语言
class _LanguageScope extends InheritedWidget {
  final LanguageProvider provider;

  const _LanguageScope({
    required this.provider,
    required super.child,
  });

  @override
  bool updateShouldNotify(_LanguageScope oldWidget) => true;
}

/// 包装 MaterialApp 的语言提供者
class LanguageWrapper extends StatefulWidget {
  final Widget child;
  const LanguageWrapper({required this.child, super.key});

  @override
  State<LanguageWrapper> createState() => _LanguageWrapperState();
}

class _LanguageWrapperState extends State<LanguageWrapper> {
  final LanguageProvider _provider = LanguageProvider();

  @override
  Widget build(BuildContext context) {
    return ListenableBuilder(
      listenable: _provider,
      builder: (context, _) {
        return _LanguageScope(
          provider: _provider,
          child: widget.child,
        );
      },
    );
  }
}

